#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	n;

	if (!s)
		return (ft_putstr("(null)"));
	n = 0;
	while (s[n])
	{
		write(1, &s[n], 1);
		n++;
	}
	return (n);
}
